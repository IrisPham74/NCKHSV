class PDU(nn.Module):  # physical block
    def __init__(self, channel):
        super(PDU, self).__init__()
        self.avg_pool = nn.AdaptiveAvgPool2d(1)
        self.ka = nn.Sequential(
            nn.Conv2d(channel, channel // 8, 1, padding=0, bias=True),
            nn.ReLU(inplace=True),
            nn.Conv2d(channel // 8, channel, 1, padding=0, bias=True),
            nn.Sigmoid()
        )
        self.td = nn.Sequential(
            default_conv(channel, channel, 3),
            default_conv(channel, channel // 8, 3),
            nn.ReLU(inplace=True),
            default_conv(channel // 8, channel, 3),
            nn.Sigmoid()
        )

    def forward(self, x):
        # print("FLAG here: ", x.shape)
        # B, L, C = x.shape
        # H = int(math.sqrt(L))
        # W = int(math.sqrt(L))
        # x = x.view(B, C, H, W)
        if len(x.shape) == 4:
            B, C, H, W = x.shape
            H = int(H)
            W = int(W)
        else:
            B, L, C = x.shape
            H = int(math.sqrt(L))
            W = int(math.sqrt(L))
        x = x.view(B, C, H, W)

        a = self.avg_pool(x)
        # print("FLAG1 here: ", a.shape)
        a = self.ka(a)
        # print("FLAG2 here: ", a.shape)
        t = self.td(x)
        j = torch.mul((1 - t), a) + torch.mul(t, x)
        return j
